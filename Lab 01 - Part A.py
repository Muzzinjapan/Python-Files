# A Calculator that converts Celsius to Fahrenheit

# Ask the user for the temperature
celsius_temp = input("Enter the temperature in Celsius:")

#Convert the temp to a float
celsius_temp = float(celsius_temp)

#Convert the temperature into Fahrenheit
fahrenheit_temp = (celsius_temp * 1.8) + 32

#Print the result
print("The temperature in Fahrenheit:", fahrenheit_temp)

